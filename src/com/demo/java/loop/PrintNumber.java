package com.demo.java.loop;

public class PrintNumber {
    //ascending
    public static void main(String[] args) {

        //ascending
        for (int i=0;i<=10;i++)
        {
            System.out.println(i);
        }
        //descending
        for (int i=10;i>=1;i--)
        {
            System.out.println(i);
        }
    }
}
